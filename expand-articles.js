function expandArticle(title, content) {
  const text = content.replace(/<[^>]*>/g, '').replace(/\s+/g, ' ').trim();
  const currentWords = text.split(/\s+/).length;

  if (currentWords >= 1500) {
    return { content, words: currentWords };
  }

  const expandedContent = content.trim() + expansionTemplate;
  const newText = expandedContent.replace(/<[^>]*>/g, '').replace(/\s+/g, ' ').trim();
  const newWords = newText.split(/\s+/).length;

  return { content: expandedContent, words: newWords };
}

