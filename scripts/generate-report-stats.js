#!/usr/bin/env node

/**
 * Generate pre-calculated report statistics from companies data
 * Run: node scripts/generate-report-stats.js
 */

import { readFileSync, writeFileSync } from 'fs';
import { join } from 'path';

const filePath = join(process.cwd(), 'lib', 'companies.json');
const outputPath = join(process.cwd(), 'lib', 'report-stats.ts');

console.log('Reading companies data...');
const data = readFileSync(filePath, 'utf-8');
const companies = JSON.parse(data);

console.log(`Processing ${companies.length} companies...`);

// Total companies
const totalCompanies = companies.length;

// Category distribution
const categoryCounts = {};
companies.forEach((c) => {
  if (c.categories && Array.isArray(c.categories)) {
    c.categories.forEach((cat) => {
      const catName = typeof cat === 'string' ? cat : cat.name;
      categoryCounts[catName] = (categoryCounts[catName] || 0) + 1;
    });
  }
});

const sortedCategories = Object.entries(categoryCounts)
  .sort((a, b) => b[1] - a[1])
  .slice(0, 10)
  .map(([name, count]) => ({ name, count }));

// Funding analysis
const withFunding = companies.filter((c) => c.funding && c.funding !== '' && c.funding !== 'N/A');
const fundingStats = {
  total: withFunding.length,
  percentage: ((withFunding.length / totalCompanies) * 100).toFixed(1),
  byStage: {},
};

withFunding.forEach((c) => {
  const f = c.funding.toLowerCase();
  if (f.includes('seed')) fundingStats.byStage['Seed'] = (fundingStats.byStage['Seed'] || 0) + 1;
  else if (f.includes('series a')) fundingStats.byStage['Series A'] = (fundingStats.byStage['Series A'] || 0) + 1;
  else if (f.includes('series b')) fundingStats.byStage['Series B'] = (fundingStats.byStage['Series B'] || 0) + 1;
  else if (f.includes('bootstrap')) fundingStats.byStage['Bootstrap'] = (fundingStats.byStage['Bootstrap'] || 0) + 1;
  else fundingStats.byStage['Other'] = (fundingStats.byStage['Other'] || 0) + 1;
});

// Location distribution
const locationCounts = {};
companies.forEach((c) => {
  if (c.headquarters) {
    const city = c.headquarters.split(',')[0].trim();
    locationCounts[city] = (locationCounts[city] || 0) + 1;
  }
});
const topLocations = Object.entries(locationCounts)
  .sort((a, b) => b[1] - a[1])
  .slice(0, 5)
  .map(([city, count]) => ({ city, count }));

// Founded years
const years = companies
  .map((c) => c.founded_year)
  .filter((y) => y && y >= 2010 && y <= 2025);
const avgFounded = years.length ? Math.round(years.reduce((a, b) => a + b, 0) / years.length) : 2018;
const newestFounded = years.length ? Math.max(...years) : 2025;

// Generate TypeScript file
const content = `// Pre-calculated report statistics
// This file is auto-generated by scripts/generate-report-stats.js
// Last updated: ${new Date().toISOString().split('T')[0]}

export const reportStats = {
  totalCompanies: ${totalCompanies},
  categoryCounts: ${JSON.stringify(sortedCategories, null, 2)},
  fundingStats: {
    total: ${fundingStats.total},
    percentage: ${fundingStats.percentage},
    byStage: ${JSON.stringify(fundingStats.byStage, null, 2)},
  },
  topLocations: ${JSON.stringify(topLocations, null, 2)},
  avgFounded: ${avgFounded},
  newestFounded: ${newestFounded},
};
`;

writeFileSync(outputPath, content, 'utf-8');
console.log(`✅ Generated ${outputPath}`);
console.log(`   Total companies: ${totalCompanies}`);
console.log(`   Categories: ${sortedCategories.length}`);
console.log(`   Funding data: ${fundingStats.total} companies`);
